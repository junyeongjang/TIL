int		is_prime(int nb)
{
	int i;
	int temp;

	i = 0;
	if (nb <= 1)
		return (0);
	temp = nb / 2;

	while (i <= temp && i <= 65536)
	{
		if (nb  % i == 0)
			return (0);
		i++;
	}
	return (1);
} 
